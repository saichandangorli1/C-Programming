#include <stdio.h>
int main()
{
    int a;
    a = 1;
    // OR
    int a = 1; // Size of the int is usually 4 bytes(32 bits)


    float b;
    b = 1;
    // OR
    float b = 1; // Size of the float is usually 4 bytes(32 bits)

    char c;
    c = 'A';
    // OR
    char c = 'A'; // Size of the char is always 1 bytes(8bits) as per C standard


    
    return 0;
}